/**
 * Bundle the zero-install browser demo (S15/T6): web/demo/src/main.js — which
 * injects MemoryTerraceNode + bots and then imports the REAL apps/terrace/app.js
 * — into a single committed ESM file the static site serves as-is (the Vercel
 * deploy neither installs nor builds; git is the source of truth for prod).
 *
 * `conditions: ["development"]` resolves @curva/* to TypeScript source, same as
 * vitest — esbuild compiles it directly, no tsc step. The Pears SDK dynamic
 * imports in autobase-runtime stay as runtime `import()`s the demo never calls.
 */
import { build } from "esbuild";

const result = await build({
  entryPoints: ["web/demo/src/main.js"],
  outfile: "web/demo/bundle.js",
  bundle: true,
  format: "esm",
  platform: "browser",
  target: ["es2022"],
  conditions: ["development"],
  minify: true,
  legalComments: "none",
  banner: {
    js: "// GENERATED by scripts/build-web-demo.mjs — do not edit. Source: web/demo/src/ + apps/terrace/app.js",
  },
  logLevel: "info",
});

if (result.errors.length > 0) process.exit(1);
